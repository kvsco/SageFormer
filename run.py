import argparse
import torch
import datetime
from sageformer import Trainer

def run_model(config, flag='train'):
    # init
    model = Trainer(config)

    if flag == 'train':
        setting = datetime.datetime.now().strftime("%Y_%m_%d_%H_%M_%S")
        model.train(setting)

        model.test(setting)
        model.predict(setting, True)
    torch.cuda.empty_cache()
    return True


